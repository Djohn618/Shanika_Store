// Initialize timeline animations
document.addEventListener('DOMContentLoaded', function() {
  initializeTimelineAnimations();
});

function initializeTimelineAnimations() {
  const observerOptions = {
    threshold: 0.2,
    rootMargin: '0px 0px -100px 0px'
  };
  
  const timelineObserver = new IntersectionObserver(function(entries) {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('animated');
      }
    });
  }, observerOptions);
  
  // Observe timeline items
  const timelineItems = document.querySelectorAll('.timeline-item');
  timelineItems.forEach((item, index) => {
    item.classList.add('animate-on-scroll');
    item.style.animationDelay = `${index * 0.2}s`;
    timelineObserver.observe(item);
  });
  
  // Observe mission and vision cards
  const missionVisionCards = document.querySelectorAll('.mission-card, .vision-card');
  missionVisionCards.forEach(card => {
    card.classList.add('animate-on-scroll');
    timelineObserver.observe(card);
  });
}

// Add parallax effect to timeline markers
window.addEventListener('scroll', function() {
  const scrolled = window.pageYOffset;
  const timelineMarkers = document.querySelectorAll('.timeline-marker');
  
  timelineMarkers.forEach((marker, index) => {
    const speed = 0.1 + (index * 0.02);
    const yPos = -(scrolled * speed);
    marker.style.transform = `translateY(${yPos}px)`;
  });
});

// Add hover effects to timeline images
document.querySelectorAll('.timeline-image img').forEach(img => {
  img.addEventListener('mouseenter', function() {
    this.style.transform = 'scale(1.05) rotate(2deg)';
  });
  
  img.addEventListener('mouseleave', function() {
    this.style.transform = 'scale(1) rotate(0deg)';
  });
});

// Add click animation to mission/vision cards
document.querySelectorAll('.mission-card, .vision-card').forEach(card => {
  card.addEventListener('click', function() {
    this.style.transform = 'scale(0.98)';
    setTimeout(() => {
      this.style.transform = '';
    }, 150);
  });
});

