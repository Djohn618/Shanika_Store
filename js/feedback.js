function initializeFeedbackAnimations() {
  const observerOptions = {
    threshold: 0.1,
    rootMargin: '0px 0px -50px 0px'
  };
  
  const observer = new IntersectionObserver(function(entries) {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('animated');
      }
    });
  }, observerOptions);
  
  // Observe elements that should animate on scroll
  const animateElements = document.querySelectorAll(
    '.stat-card, .review-card, .feedback-form-container'
  );
  
  animateElements.forEach((el, index) => {
    el.classList.add('animate-on-scroll');
    el.style.animationDelay = `${index * 0.1}s`;
    observer.observe(el);
  });
}

function initializeFeedbackForm() {
  const form = document.getElementById('feedbackForm');
  
  if (form) {
    form.addEventListener('submit', handleFeedbackSubmit);
    
    // Add real-time validation
    const inputs = form.querySelectorAll('input, select, textarea');
    inputs.forEach(input => {
      input.addEventListener('blur', validateField);
      input.addEventListener('input', clearFieldError);
    });
  }
}

function initializeRatingSystem() {
  const ratingInputs = document.querySelectorAll('#ratingInput input');
  const ratingText = document.getElementById('ratingText');
  
  ratingInputs.forEach(input => {
    input.addEventListener('change', function() {
      const rating = parseInt(this.value);
      const textKey = ratingTexts[rating];
      
      if (textKey && translations[currentLanguage] && translations[currentLanguage][textKey]) {
        ratingText.textContent = translations[currentLanguage][textKey];
        ratingText.classList.add('show');
      }
    });
  });
}

function initializeCategoryRatings() {
  const categoryRatings = document.querySelectorAll('.mini-rating');
  
  categoryRatings.forEach(rating => {
    const stars = rating.querySelectorAll('i');
    
    stars.forEach((star, index) => {
      star.addEventListener('click', function() {
        const value = parseInt(this.getAttribute('data-value'));
        
        // Clear all stars in this category
        stars.forEach(s => s.classList.remove('active'));
        
        // Activate stars up to clicked value
        for (let i = 0; i < value; i++) {
          stars[i].classList.add('active');
        }
        
        // Store the rating value
        rating.setAttribute('data-rating', value);
      });
      
      star.addEventListener('mouseenter', function() {
        const value = parseInt(this.getAttribute('data-value'));
        
        // Highlight stars on hover
        stars.forEach((s, i) => {
          if (i < value) {
            s.style.color = '#fbbf24';
          } else {
            s.style.color = '#e5e7eb';
          }
        });
      });
    });
    
    rating.addEventListener('mouseleave', function() {
      const currentRating = parseInt(this.getAttribute('data-rating')) || 0;
      
      // Reset to current rating
      stars.forEach((s, i) => {
        if (i < currentRating) {
          s.style.color = '#fbbf24';
        } else {
          s.style.color = '#e5e7eb';
        }
      });
    });
  });
}

function handleFeedbackSubmit(e) {
  e.preventDefault();
  
  const form = e.target;
  const formData = new FormData(form);
  
  // Validate form
  if (!validateFeedbackForm(form)) {
    return;
  }
  
  // Collect category ratings
  const categoryRatings = {};
  document.querySelectorAll('.mini-rating').forEach(rating => {
    const category = rating.getAttribute('data-category');
    const value = rating.getAttribute('data-rating');
    if (value) {
      categoryRatings[category] = parseInt(value);
    }
  });
  
  // Show loading state
  form.classList.add('loading');
  
  // Simulate form submission (replace with actual submission logic)
  setTimeout(() => {
    form.classList.remove('loading');
    showFeedbackSuccess();
    form.reset();
    resetRatings();
  }, 2000);
}

function validateFeedbackForm(form) {
  let isValid = true;
  const requiredFields = form.querySelectorAll('[required]');
  
  requiredFields.forEach(field => {
    if (!validateField({ target: field })) {
      isValid = false;
    }
  });
  
  // Check if overall rating is selected
  const ratingSelected = form.querySelector('input[name="rating"]:checked');
  if (!ratingSelected) {
    showFormError('Please select an overall rating');
    isValid = false;
  }
  
  return isValid;
}

function validateField(e) {
  const field = e.target;
  const value = field.value.trim();
  let isValid = true;
  
  // Remove existing error
  clearFieldError(e);
  
  // Check if required field is empty
  if (field.hasAttribute('required') && !value) {
    showFieldError(field, 'This field is required');
    isValid = false;
  }
  
  // Email validation
  if (field.type === 'email' && value) {
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    if (!emailRegex.test(value)) {
      showFieldError(field, 'Please enter a valid email address');
      isValid = false;
    }
  }
  
  return isValid;
}

function showFieldError(field, message) {
  field.style.borderColor = '#ef4444';
  
  // Create error message if it doesn't exist
  let errorMsg = field.parentNode.querySelector('.error-message');
  if (!errorMsg) {
    errorMsg = document.createElement('div');
    errorMsg.className = 'error-message';
    errorMsg.style.color = '#ef4444';
    errorMsg.style.fontSize = '0.875rem';
    errorMsg.style.marginTop = '0.25rem';
    field.parentNode.appendChild(errorMsg);
  }
  
  errorMsg.textContent = message;
}

function clearFieldError(e) {
  const field = e.target;
  field.style.borderColor = '';
  
  const errorMsg = field.parentNode.querySelector('.error-message');
  if (errorMsg) {
    errorMsg.remove();
  }
}

function showFormError(message) {
  const form = document.getElementById('feedbackForm');
  let errorDiv = form.querySelector('.form-error');
  
  if (!errorDiv) {
    errorDiv = document.createElement('div');
    errorDiv.className = 'form-error';
    errorDiv.style.cssText = `
      background: #ef4444;
      color: white;
      padding: 1rem;
      border-radius: 0.5rem;
      margin-bottom: 1rem;
      text-align: center;
    `;
    form.insertBefore(errorDiv, form.firstChild);
  }
  
  errorDiv.textContent = message;
  
  // Remove error after 5 seconds
  setTimeout(() => {
    errorDiv.remove();
  }, 5000);
}

function showFeedbackSuccess() {
  const form = document.getElementById('feedbackForm');
  const successDiv = document.createElement('div');
  successDiv.className = 'form-success';
  successDiv.innerHTML = `
    <div style="display: flex; align-items: center; justify-content: center; gap: 0.5rem;">
      <i class="fas fa-check-circle"></i>
      <span>Thank you for your feedback! We appreciate your input.</span>
    </div>
  `;
  
  form.insertBefore(successDiv, form.firstChild);
  
  // Remove success message after 5 seconds
  setTimeout(() => {
    successDiv.remove();
  }, 5000);
}

function resetRatings() {
  // Reset overall rating
  document.querySelectorAll('#ratingInput input').forEach(input => {
    input.checked = false;
  });
  
  // Reset rating text
  const ratingText = document.getElementById('ratingText');
  ratingText.textContent = '';
  ratingText.classList.remove('show');
  
  // Reset category ratings
  document.querySelectorAll('.mini-rating').forEach(rating => {
    rating.removeAttribute('data-rating');
    rating.querySelectorAll('i').forEach(star => {
      star.classList.remove('active');
      star.style.color = '#e5e7eb';
    });
  });
}

function animateStats() {
  const statNumbers = document.querySelectorAll('.stat-number');
  
  const observer = new IntersectionObserver(function(entries) {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        animateNumber(entry.target);
        observer.unobserve(entry.target);
      }
    });
  });
  
  statNumbers.forEach(stat => {
    observer.observe(stat);
  });
}

function animateNumber(element) {
  const text = element.textContent;
  const number = parseFloat(text.replace(/[^\d.]/g, ''));
  const suffix = text.replace(/[\d.]/g, '');
  
  if (isNaN(number)) return;
  
  const duration = 2000;
  const steps = 60;
  const increment = number / steps;
  let current = 0;
  
  const timer = setInterval(() => {
    current += increment;
    if (current >= number) {
      current = number;
      clearInterval(timer);
    }
    
    if (text.includes('.')) {
      element.textContent = current.toFixed(1) + suffix;
    } else {
      element.textContent = Math.floor(current) + suffix;
    }
  }, duration / steps);
}

